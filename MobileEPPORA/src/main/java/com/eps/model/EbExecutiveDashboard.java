package com.eps.model;

public class EbExecutiveDashboard {

	private String projectName;
	private String startDate;
	private String endDate;
	private String estimatedHours;
	private String expendedHours;
	private String estimatedCost;
	private String expendedToDate;
	private String spi;
	private String cpi;
	public String getProjectName() {
		return projectName;
	}
	public void setProjectName(String projectName) {
		this.projectName = projectName;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getEstimatedHours() {
		return estimatedHours;
	}
	public void setEstimatedHours(String estimatedHours) {
		this.estimatedHours = estimatedHours;
	}
	public String getExpendedHours() {
		return expendedHours;
	}
	public void setExpendedHours(String expendedHours) {
		this.expendedHours = expendedHours;
	}
	public String getEstimatedCost() {
		return estimatedCost;
	}
	public void setEstimatedCost(String estimatedCost) {
		this.estimatedCost = estimatedCost;
	}
	public String getExpendedToDate() {
		return expendedToDate;
	}
	public void setExpendedToDate(String expendedToDate) {
		this.expendedToDate = expendedToDate;
	}
	public String getSpi() {
		return spi;
	}
	public void setSpi(String sPI) {
		spi = sPI;
	}
	public String getCpi() {
		return cpi;
	}
	public void setCpi(String cPI) {
		cpi = cPI;
	}
	
	
}
