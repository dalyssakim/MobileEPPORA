package com.eps.model;

public class EbProgress {

	private String description;
	private String plannedQuantity;
	private String incrementalProgress;
	private String accomplishedToDate;
	private String progressId;
	
	
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPlannedQuantity() {
		return plannedQuantity;
	}
	public void setPlannedQuantity(String plannedQuantity) {
		this.plannedQuantity = plannedQuantity;
	}
	public String getIncrementalProgress() {
		return incrementalProgress;
	}
	public void setIncrementalProgress(String incrementalProgress) {
		this.incrementalProgress = incrementalProgress;
	}
	public String getAccomplishedToDate() {
		return accomplishedToDate;
	}
	public void setAccomplishedToDate(String accomplishedToDate) {
		this.accomplishedToDate = accomplishedToDate;
	}
	public String getProgressId() {
		return progressId;
	}
	public void setProgressId(String progressId) {
		this.progressId = progressId;
	}
	
	
}
